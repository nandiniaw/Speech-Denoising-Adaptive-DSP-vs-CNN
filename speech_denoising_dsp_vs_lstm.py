def mix_speech_noise(clean, noise, snr_db):
    # Power of clean speech
    speech_power = np.mean(clean**2)

    # Power of noise
    noise_power = np.mean(noise**2)

    # Required noise power for given SNR
    target_noise_power = speech_power / (10**(snr_db / 10))

    # Scale noise
    noise_scaled = noise * np.sqrt(target_noise_power / (noise_power + EPS))

    # Mix
    noisy = clean + noise_scaled

    return noisy, noise_scaled

