def dataset_generator():
    while True:
        speech_path = np.random.choice(speech_files)
        noise_path = np.random.choice(noise_files)
        snr = np.random.choice(SNR_RANGE)

        clean = load_audio(speech_path)
        noise = load_audio(noise_path)

        noisy = add_noise(clean, noise, snr)

        clean_spec = to_spectrogram(clean)
        noisy_spec = to_spectrogram(noisy)

        yield noisy_spec[..., np.newaxis], clean_spec[..., np.newaxis]

