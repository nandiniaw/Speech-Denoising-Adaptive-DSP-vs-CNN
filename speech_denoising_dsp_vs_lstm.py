import tensorflow as tf
SR = 8000          # Sampling rate
N_FFT = 256        # FFT size
HOP_LENGTH = 128   # Overlap
SEGMENT_LEN = SR * 1  # 1 second segments
SNR_RANGE = [-5, 0, 5, 10]
def load_audio(file_path, length=SEGMENT_LEN):
    audio, _ = librosa.load(file_path, sr=SR)

    if len(audio) < length:
        audio = np.pad(audio, (0, length - len(audio)))
    else:
        start = np.random.randint(0, len(audio) - length)
        audio = audio[start:start + length]

    return audio
def add_noise(clean, noise, snr_db):
    clean_power = np.mean(clean**2)
    noise_power = np.mean(noise**2)

    snr_linear = 10 ** (snr_db / 10)
    scaling_factor = np.sqrt(clean_power / (snr_linear * noise_power))

    return clean + scaling_factor * noise
def to_spectrogram(signal):
    spec = librosa.stft(signal, n_fft=N_FFT, hop_length=HOP_LENGTH)
    mag = np.abs(spec)
    log_mag = np.log1p(mag)
    return log_mag
speech_files = [f"/content/speech_clean/{f}"
                for f in os.listdir("/content/speech_clean")]

noise_files = [f"/content/noise/{f}"
               for f in os.listdir("/content/noise")]

