def add_colored_noise(signal, snr_db):
    white = np.random.randn(len(signal))

    # AR(1) colored noise
    colored = np.convolve(white, [1, -0.9], mode='same')
    colored /= np.sqrt(np.mean(colored**2))

    signal_power = np.mean(signal**2)
    noise_power = signal_power / (10**(snr_db/10))

    noisy = signal + np.sqrt(noise_power) * colored
    return noisy, np.sqrt(noise_power) * colored


