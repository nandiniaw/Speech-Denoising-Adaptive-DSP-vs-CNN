def nlms_filter(primary, reference, mu=0.8, L=64, eps=1e-6):
    N = len(primary)
    w = np.zeros(L)
    y = np.zeros(N)
    e = np.zeros(N)

    for n in range(L, N):
        x = reference[n-L:n][::-1]
        y[n] = np.dot(w, x)
        e[n] = primary[n] - y[n]

        norm = np.dot(x, x) + eps
        w += (mu / norm) * e[n] * x

    return e

