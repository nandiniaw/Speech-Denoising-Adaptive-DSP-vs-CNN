def add_awgn(signal, snr_db):
    signal_power = np.mean(signal**2)
    noise_power = signal_power / (10**(snr_db / 10))
    noise = np.sqrt(noise_power) * np.random.randn(len(signal))
    return signal + noise
def calculate_snr(clean, noisy):
    noise = noisy - clean
    return 10 * np.log10(np.mean(clean**2) / np.mean(noise**2))
def wiener_filter(noisy, clean, fs, n_fft=512, hop=256):
    Y = librosa.stft(noisy, n_fft=n_fft, hop_length=hop)
    S = librosa.stft(clean, n_fft=n_fft, hop_length=hop)

    Ps = np.mean(np.abs(S)**2, axis=1, keepdims=True)
    Pn = np.mean(np.abs(Y - S)**2, axis=1, keepdims=True)

    H = Ps / (Ps + Pn + 1e-10)

    Y_hat = H * Y
    return librosa.istft(Y_hat, hop_length=hop)
